# 以下写法正确，但是在leetcode提交返回超时
select  dept.name Department,emp.name Employee, emp.salary  Salary
from employee emp
left join department dept on dept.id = emp.departmentid
where emp.salary = (select max(emp2.salary) from employee emp2 where emp2.departmentid=dept.id) order by emp.id;

